package items;

public class Category {
	public int lid;
	public String name;
	public int isdel;
	
	public Category() {}

	public int getLid() {
		return lid;
	}

	public String getName() {
		return name;
	}

	public int getIsdel() {
		return isdel;
	}

	public void setLid(int lid) {
		this.lid = lid;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setIsdel(int isdel) {
		this.isdel = isdel;
	}
	
	
}
